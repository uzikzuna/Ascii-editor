export const FONTS: Record<string, FigletFont> = {
  Slant: SLANT_FONT,
  Blocks: BLOCKS_FONT,
  Cyber: CYBER_FONT,
  Mini: MINI_FONT
};

export function generateTextBanner(text: string, fontName: string, spacing: number = 1): string {
  const font = FONTS[fontName] || SLANT_FONT;
  const result: string[] = Array(font.height).fill('');
  
  const cleanText = text.toUpperCase();
  const spaces = ' '.repeat(spacing);
  
  for (let i = 0; i < cleanText.length; i++) {
    const char = cleanText[i];
    const charRep = font.chars[char] || font.chars['?'] || Array(font.height).fill(' '.repeat(3));
    
    for (let r = 0; r < font.height; r++) {
      result[r] += (charRep[r] || ' '.repeat(charRep[0]?.length || 3)) + spaces;
    }
  }
  
  return result.join('\n');
}
